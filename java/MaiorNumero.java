public class MaiorNumero {
    public static void main(String[] args) {
        int a = 10, b = 20;

        if (a > b) {
            System.out.println("A é maior");
        } else if (a == b) {
            System.out.println("São iguais");
        } else {
            System.out.println("B é maior");
        }
    }
}